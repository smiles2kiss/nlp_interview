class Solution {
public:
    int wiggleMaxLength(vector<int>& nums) {
        if (nums.size() < 2)
            return nums.size();

        int up = 1;
        int down = 1;
        for (int i = 1; i < nums.size(); i++) {
            if (nums[i] > nums[i-1]) // 连续上升加一次，等于转折点加一次
                up = down + 1;
            else if (nums[i] < nums[i - 1]) // 连续上升加一次，等于转折点一次
                down = up + 1;
        }
        return max(down, up);
    }
};
